package com.dz.visitcounter.view;

public class PeriodStats {

    public Integer totalVisits;

    public Integer totalUniqueVisits;

    public Integer totalRegularVisitors;

    public Integer getTotalVisits() {
        return totalVisits;
    }

    public void setTotalVisits(Integer totalVisits) {
        this.totalVisits = totalVisits;
    }

    public Integer getTotalUniqueVisits() {
        return totalUniqueVisits;
    }

    public void setTotalUniqueVisits(Integer totalUniqueVisits) {
        this.totalUniqueVisits = totalUniqueVisits;
    }

    public Integer getTotalRegularVisitors() {
        return totalRegularVisitors;
    }

    public void setTotalRegularVisitors(Integer totalRegularVisitors) {
        this.totalRegularVisitors = totalRegularVisitors;
    }
}
